# Loop
# Repeating a block of code multiple times.
# range(start,stop-1,step)
for i in range(0, 10, 1):
    print(i)
for i in range(0, 10, 2):
    print(i)
for i in range(10, -1, -1):
    print(i)
for i in range(10):
    print(i)
for _ in range(4):
    print("udhfdf", _)
