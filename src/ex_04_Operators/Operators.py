# Unary Operator +, -
age = +65
age2 = -65

print(age, age2)

# Ternary Operator similar to if else statement
user_age = 12
print("you can go to goa and vote" if user_age >= 18 else "No you cannot go to goa and vote")

# Arthematic operators +, - , *, /
print(67/5)
print(67+5)
print(67-5)
print(67*5)

# Modulus operator % reminder
print(4 % 3)

# Floor operator // gives quotient in int
print(67//5)

# Comparative operator == (True or False)
print(67 == "rtrs")

# not Operator
is_love_true = False
print(not is_love_true)

# Logical Operator >,<,<=,>= (True or False)
print(3 < 7)
print(7 > 23)
print(10 <= 10)

# and , or (True or False)

print(45 % 2 == 1 and 67 % 2 == 1)  # true
print(45 % 2 == 1 or 67 % 2 == 1)  # true
print(45 % 2 == 0 or 67 % 2 == 1)  # true
print(45 % 2 == 0 and 67 % 2 == 1)  # false
print(45 % 2 == 0 or 67 % 2 == 0)  # false

# Increment (+=) / Decrement (-=) / (*=) /  Operators

