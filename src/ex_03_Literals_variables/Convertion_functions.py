# anything to string str()
print(str(45), type(str(45)))

# anything to integer int()
print(int("5678"), type(int("5678")))

# anything to Decimal float()
print(float("5678"), type(float("5678")))

# anything to Bolean bool()
print(bool(45), type(bool(45)))
print(bool("5678"), type(bool("5678")))
print(bool(None), type(bool(None)))  # None values will be False
print(bool(), type(bool()))  # empty values will be False
