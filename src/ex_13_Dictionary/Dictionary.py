# Dictionary
# Key Value Pair
# Syntax { key:value }
# Dictionary is Unordered, mutable, and indexed collection of key-value pairs in Python.

my_dict = {
    "name": "Aman",
    "age": 34,
    "role": "SDET",
    "experience": 3,
    "married": True
           }

print(my_dict)


